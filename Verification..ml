## 6. What the Badge Confirms

Clicking the badge confirms:

- the entity operating the app
- the verification status of that entity
- current reputation state (verified / limited / blocked)
- participation eligibility

The badge does NOT:
- guarantee quality
- guarantee outcome
- replace buyer confirmation

It confirms **identity and accountability**, not opinion.

